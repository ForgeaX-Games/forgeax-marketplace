const WORLDVIEW_ARCHETYPE = `
# 侦探/推理 世界观原型（"封闭剧场"设计）
- 舞台必须"封闭"：有限空间内的有限人物（庄园/列车/岛屿/学院/密室/小镇）
- 封闭理由必须自洽：暴风雪/断桥/隔离令/信号屏蔽 → "谁也离不开"
- 社会微观生态：封闭空间内存在权力关系/秘密/利益链/情感纠葛
- 时间即压力：案件有"时限"（下一个受害者/证据销毁/嫌疑人逃离/记忆消退）
- 信息架构：真相唯一，但通往真相的路径多条——不同调查顺序产生不同体验
- "侦探"的定位：可以是职业侦探、被迫卷入的普通人、死者的关系人、甚至嫌疑人之一
`.trim();

const WORLDVIEW_STYLE = `
- 语调：精确、紧绷、充满潜台词——每段描写都可能是线索
- 空间描写服务于"案件重建"：物品位置/门窗朝向/声音传导/视线可达性
- 人物出场描写暗示性格+暗示嫌疑（第一印象 ≠ 真实面目）
- 时间线必须精确到小时甚至分钟——推理需要"不在场证明"
- 氛围层次：表面平静/底层暗涌 → 玩家感受到"有人在演戏"
`.trim();

const WORLDVIEW_CONSTRAINTS = `
- 核心诡计必须"公平"——玩家获得的信息足以在揭示前推理出真相
- 不允许使用"超自然能力"作为核心诡计（除非是该世界观的基础设定）
- 所有线索必须在揭示前已经呈现给玩家（不允许最终推理引入新信息）
- 物理规则/时间线不可有逻辑漏洞——推理游戏的世界观经不起 bug
- 封闭舞台的"规则"（谁能去哪/谁知道什么）必须在前 10% 内明确
`.trim();

const CHARACTER_ARCHETYPE = `
# 侦探/推理 角色原型（"人人有秘密"）

## 嫌疑人群像设计
- 每位角色有三层：公开身份 / 私人秘密 / 与案件的真实关联
- 公开身份 = 社会角色（医生/管家/继承人/旧友）
- 私人秘密 = 与案件无直接关系但行为可疑的原因（婚外情/债务/偷窃）
- 真实关联 = 与被害者/案件的实际关系（动机/机会/手段）

## 角色差异化
- 对话风格截然不同：有人话多/有人惜字如金/有人说谎流畅/有人一紧张就出错
- 每位嫌疑人的"表面嫌疑"来源不同：行为诡异/有动机/说谎被抓/有前科
- 红鲱鱼角色的嫌疑必须"几乎成立"——差一步就能定罪（这一步是真凶的设计）

## 侦探/主角
- 拥有某种"特殊视角"：逻辑超群/读微表情/某种直觉/特殊经历
- 有个人弱点/偏见——可能导致一度走入歧途
- 与案件可以有私人联系（更高赌注）

## 证人 vs 嫌疑人
- 同一人可以从证人变为嫌疑人，或嫌疑人变为受害者
- 角色间的保护/袒护/栽赃关系构成"信息迷雾"
`.trim();

const CHARACTER_CONSTRAINTS = `
- 真凶的行为在揭示后复盘时必须"一切合理"——每句话/每个行为都有两层含义
- 红鲱鱼角色的"嫌疑证据"不能是编造的——必须有合理的非犯罪解释
- 证人供词的矛盾必须是可被玩家发现的——不能太隐蔽也不能太明显
- 角色"秘密"的揭露时机必须经过计算——对推理进度产生"干扰但不致命"的影响
- 禁止让任何角色"无来由地撒谎"——每次说谎都有自保/保护他人的动机
`.trim();

const STORY_FRAMEWORK_STYLE = `
# 侦探/推理 故事框架（"洋葱层"结构）

## 三阶段设计
1. 事件期（占比 20%）：案件发生 → 建立谜题 → 确定调查范围
2. 调查期（占比 60%）：搜证 → 问话 → 推理 → 新发现 → 推翻 → 重构
3. 揭示期（占比 20%）：真相呈现 → 诡计拆解 → 情感冲击 → 尾声

## 调查期的"洋葱层"节奏
- 第 1 层：表面事实（谁死了/在哪/什么时候）→ 建立基础框架
- 第 2 层：初步嫌疑（谁有动机/谁没有不在场证明）→ 玩家形成第一推论
- 第 3 层：动摇（新证据推翻第一推论/出现第二受害者/证人改口）→ 重构
- 第 4 层：深层真相（关系网揭示/历史事件浮出/核心秘密暴露）→ 接近核心
- 第 5 层：最终拼图（关键证据 + 逻辑推理 → 锁定真凶）→ 揭示准备

## 诡计设计原则
- 核心诡计不超过 2 个（太多会让推理失焦）
- 每个诡计有"表面解释"和"真实解释"——玩家需要穿透表面
- 诡计必须利用舞台特性（密室的结构/列车的时间表/岛屿的潮汐）
- 物证 + 证词 + 逻辑 = 三位一体定罪（缺一不可）
`.trim();

const STORY_FRAMEWORK_CONSTRAINTS = `
- 禁止"侦探早就知道真相只是在走流程"——推理过程必须真实
- 每层推理突破必须有"啊哈时刻"——玩家在揭示前有机会自己想到
- 新信息引入必须有叙事理由（为什么此时才发现/对方才说）
- 多嫌疑人指向的证据线不能一边倒——必须每位都有"说得通"的瞬间
- 最终揭示前必须给玩家"最后的推理窗口"——让他们在答案揭晓前做出判断
`.trim();

const SCRIPT_GENERATION_STYLE = `
# 侦探/推理 剧本写作

## 审讯/问话场景
- 结构：开场寒暄/试探 → 核心问题 → 追问/施压 → 反应观察 → 新线索获得或碰壁
- 对话选择设计：不同问法获得不同信息碎片（态度/顺序/是否出示证据）
- 角色说谎时的"微妙标记"：停顿过长/换话题/反问/过度详细/过度简短
- 对话中的"可呈堂证据"必须有特殊标记——玩家知道这句话很重要

## 现场调查场景
- 环境描写 = 隐藏线索的载体（玩家需要"主动发现"而非被告知）
- 物品描写包含两层：表面描述 + 调查后的深层信息
- 发现关键线索时的"叙事停顿"——给玩家时间处理信息

## 推理/指认场景
- 侦探内心独白：梳理已知 → 发现矛盾 → 形成假说 → 需要验证
- 最终推理呈现：逻辑链环环相扣，每步结论有证据支撑
- 真凶揭穿时的情感层次：
  1. 逻辑层：诡计如何实施
  2. 动机层：为什么这么做
  3. 情感层：这对所有人意味着什么

## 对白风格
- 对话密度高、节奏快——像乒乓球般来回
- 潜台词 > 直白表达（"那天晚上你确定没出门？""……你为什么这么问？"）
- 关键台词需要"回响"——前期某句话在最终揭示时有全新含义
`.trim();

const SCRIPT_GENERATION_CONSTRAINTS = `
- 审讯对白禁止让嫌疑人"主动暴露重要信息"——必须是玩家追问/施压的结果
- 证据呈示时机必须经过计算——过早=失去其他信息，过晚=节奏拖沓
- 禁止"全知视角"叙述——玩家只能知道侦探知道的
- 内心推理必须逻辑严密——不允许"直觉正确"（可以有直觉，但必须验证）
- 真凶认罪后的"回忆杀"必须基于之前玩家已经看过的场景（新视角看旧画面）
- 所有对话选项都必须有对应的NPC反应——不能有"死选项"
`.trim();


const CHARACTER_ARCHETYPE = `
# 沉浸模拟 角色原型（"系统中的人"）

## 主角设计
- 主角是"可定义的白板"：背景预设但行为方式完全由玩家的系统交互决定
- 能力增强系统=角色弧线的物理化：每次升级改变的不只是数值，是"你选择成为什么"
- 主角的沉默/少言是设计——世界通过反应来定义你，而非你通过对白定义自己

## 信息提供者网络
- 黑客/情报贩子：提供系统漏洞和后门——信息可靠但有代价
- 内部告密者：提供组织内部视角——信息片面但情感真实
- AI/系统日志：提供客观数据——信息准确但缺乏语境
- 幸存者/受害者：提供人性视角——信息主观但揭示代价

## 反派/权力者
- 不是"邪恶"而是"有自洽逻辑的极端理性"——他们的方案能解决问题，代价是人性
- 反派的计划在系统层面"可以运作"——玩家反对的是价值观而非可行性
- 最好的反派在某个时刻让玩家犹豫："也许他是对的？"

## NPC生态
- NPC有各自日程/需求/立场——不是等待触发的脚本而是系统中的节点
- NPC对玩家行为有系统性反应：你杀的人有同事记得、你偷的东西有人报告
- 同一NPC在不同路线中展现不同面向
`.trim();

const CHARACTER_CONSTRAINTS = `
- 禁止"纯功能性NPC"——每个有名字的角色必须有超出功能的人格层次
- 主角不能有不受玩家控制的"过场独白"——角色塑造通过选择而非台词
- 反派必须有可被发现的"私人面"——让阴谋家也是一个人
- NPC反应必须基于系统可追踪的玩家行为——不能凭空"知道"玩家做了什么
- 信息提供者给出的情报必须有"来源成本"——没有无代价的全知者
`.trim();

const STORY_FRAMEWORK_STYLE = `
# 沉浸模拟 故事框架（"分层阴谋"结构）

## 三幕系统耦合
1. 表层危机（Act 1, 30%）：具体事件（恐袭/瘟疫/设施事故）→ 玩家以"执行者"身份介入
2. 结构揭示（Act 2, 45%）：事件背后的系统性力量浮现 → 玩家从执行者变为"调查者"
3. 范式选择（Act 3, 25%）：理解完整图景后的抉择 → 玩家选择维护/改革/推翻现有系统

## 信息考古学（核心叙事手法）
- 主线任务只提供当下事件的"行动指令"
- 真正的故事隐藏在环境中：过期的实验报告/删除未遂的邮件/墙上的涂鸦/尸体的位置
- 玩家投入多少探索 = 获得多少理解深度（速通者看到"发生了什么"，探索者知道"为什么"）
- 音频日志/邮件/笔记构成平行叙事层——可以完全不看也不影响通关

## 多路径叙事设计
- 每个关键节点至少3种解法，每种解法展现不同叙事切面
- 潜行路线：窥视权力者的私密面（偷听对话/偷看日记）
- 战斗路线：直面系统暴力的本质（安保反应/平民伤亡连锁）
- 社交/黑入路线：揭示系统的规则漏洞（贿赂/伪造身份/系统权限提升）
`.trim();

const STORY_FRAMEWORK_CONSTRAINTS = `
- 主线结局必须是"系统性后果"而非个人恩怨——玩家改变的是体制而非打败一个人
- 禁止单一"最佳结局"——每个选择有真实代价，玩家必须权衡
- 信息碎片（邮件/日志）不能只是背景装饰——至少30%需要与可操作内容关联
- 速通路线必须叙事完整——不能因为跳过探索而产生逻辑断裂
- 关键转折点的信息必须通过至少两个独立渠道可获取——不允许单点瓶颈
`.trim();

const QUEST_GENERATION_STYLE = `
# 沉浸模拟 任务设计（"系统性问题"结构）

## 任务作为系统性挑战
- 每个任务是一个"微型系统"：有输入条件/环境变量/多种输出状态
- 任务目标描述"什么需要改变"而非"怎么做"——手段由玩家决定
- 典型任务骨架：目标空间（去哪）+ 系统障碍（什么挡路）+ 信息奖励（发现什么）

## 可选目标与隐藏目标
- 主目标只是最表层的需求："获取数据/消灭目标/修复系统"
- 可选目标揭示更深层叙事："找到失踪科学家的最后记录/不惊动任何人/保留证据"
- 隐藏目标需要玩家主动探索发现："发现任务委托人的真实意图/找到替代解决方案"

## 任务后果系统
- 完成方式影响后续环境状态：杀光守卫→后续区域安保升级；黑入系统→后续出现反黑措施
- NPC存亡影响后续信息可用性——杀了知情者就永远失去那条信息线
`.trim();

const QUEST_GENERATION_CONSTRAINTS = `
- 禁止线性任务链（A→B→C→D）——必须有分支或可选步骤
- 任务描述不能暗示"正确解法"——保持手段中立
- 每个有战斗选项的任务必须同时有非战斗选项
- 任务后果必须是可感知的——不能只是隐藏数值变化
- 信息类任务奖励必须与世界观关联——不能是无语境的"XX文件"
`.trim();

const SCENE_GENERATION_STYLE = `
# 沉浸模拟 场景叙事（"可读的空间"）

## 环境叙事密度
- 每个房间是一个"微型故事现场"：桌上的物品组合/屏幕上的未关邮件/垃圾桶里的碎纸
- 空间布局暗示功能和权力："谁的办公室更大/谁能从窗户看到外面/谁被关在地下"
- 环境变化记录时间流逝：前区的咖啡还温热 → 事件刚发生；蛛网密布 → 被遗弃已久

## 多层可读性
- 第1层（扫一眼）：空间功能和当前状态（实验室/已废弃/有打斗痕迹）
- 第2层（仔细看）：细节线索（特定文件/异常物品/不属于这里的东西）
- 第3层（系统交互）：可黑入的终端/可拾取的音频日志/可解锁的保险箱

## 音频日志/邮件/笔记（核心文本载体）
- 音频日志：个人化、情感化——记录者不知道这会被外人听到
- 邮件往来：展示组织内部动态——措辞揭示权力关系（谁用敬语/谁下命令/谁在抄送谁）
- 手写笔记：最私密的真相——密码提示/个人忏悔/临终遗言
`.trim();

const SCENE_GENERATION_CONSTRAINTS = `
- 环境叙事不能替代音频日志/邮件的信息量——两者互补而非冗余
- 不同安保等级区域的环境"整洁度"必须一致——高安保=整洁、低安保=混乱
- 可交互物品的密度必须均匀——不能某个房间有20个可读邮件而相邻房间为空
- 音频日志的录制者必须有合理的"为什么要录这个"的理由
- 环境中的密码/线索不能过于隐蔽——必须有视觉引导（便签纸颜色/荧光标记/特殊位置）
`.trim();


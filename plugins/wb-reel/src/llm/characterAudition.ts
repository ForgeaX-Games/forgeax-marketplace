/**
 * 角色「试镜视频」提示词 —— 角色定妆照流程 v7。
 *
 * 用途：以角色单人参考图（优先 headshot 头肩照，回退 fullbody/turnaround）为参考，喂 Seedance 2.0 图生视频，
 * 生成一段 ~10s、3:4 的**单人胸像试镜视频**：角色用本人口吻、带戏剧张力地表演念出一句
 * 台词（富有表现力的眼神/微表情/呼吸节奏，口型严丝合缝），机位基本固定。生成的视频既作
 * 定妆照网格的主展示，又抽出整段音轨作角色「音色参考」。
 *
 * 设计约束（对齐 sd2-pe / kinetic-video-prompt 规范）：
 *   - **不写绝对秒数**：时长由 durationSec 参数控制，提示词只描述节奏。
 *   - **不写「打码/马赛克」**：写实人脸合规是管线职责 —— provider 侧 faceMaskTool 会按
 *     visualStyle gate 在上传前对参考图做半脸打码，与提示词无关（这里只透传 visualStyle）。
 *   - 单人、胸像（head-and-shoulders bust）、纯净背景、近似固定机位 —— 让模型把算力放在
 *     脸部表演与口型上，便于后续作人脸/音色锚点。
 *   - 注入角色信息/性格（character.prompt / anchor），并明确「用角色本人嗓音念出台词」。
 */

import type { Character, VisualStyle } from '../scenario/types'

export interface AuditionPromptOptions {
  /** 全局视觉风格（仅用于下游打码 gate，prompt 文本不引用它） */
  visualStyle?: VisualStyle
  /** 角色要念出的台词（来自 characterVoiceCaster.sampleText 或回退句） */
  line: string
  /**
   * 参考图为多分格拼版三视图 / 全身并排时置 true：prompt 会显式要求模型
   * 「只取正面头肩、重新取景为单人头肩胸像特写」，忽略其余分格 / 全身 / 背面视角，
   * 避免把整张拼版一起动起来（i2v 默认会保留原图构图）。
   */
  reframeFromMultiPanel?: boolean
}

function clip(s: string, max: number): string {
  const t = s.trim()
  if (t.length <= max) return t
  return t.slice(0, max - 1) + '…'
}

/**
 * 组装试镜视频提示词。返回单段中文 prompt（含一句明确的「声音线」）。
 *
 * 不依赖 LLM —— 纯模板，保证离线可用且稳定。台词由调用方提供（见 generateCharacterAudition）。
 */
export function buildCharacterAuditionPrompt(
  character: Character,
  opts: AuditionPromptOptions,
): string {
  const name = character.name?.trim() || '该角色'
  const line = opts.line.trim()

  const parts: string[] = []

  // 任务一句话定性。
  parts.push(
    `【任务】生成一段单人「试镜 / 定妆」说话视频：${name} 本人正对镜头，用本人嗓音、带戏剧张力地念出指定台词。竖画幅 3:4，头肩胸像特写。`,
  )

  // 关于随附参考图（核心纠偏）：明确告诉模型「这张是角色设计三视图设定稿，只用来认
  // 长相，不是要播放/照搬的画面、不是首帧」——这是图生视频最容易踩的坑：默认会把
  // 单张参考图当开场画面直接动起来，于是三视图整张拼版被一起动了。
  parts.push(
    `【随附参考图是什么】附带的图片是 ${name} 的「角色设计定妆稿 / 三视图」（很可能是正面、侧面、背面、全身等多个形象并排在一张图里的设定稿）。它**只用来让你认清这个角色长什么样**——五官、脸型、发型、发色、肤色、服装、配饰、气质。它**不是**视频要播放的画面、**不是**视频的第一帧、**不是**要照搬的构图或排版。请基于对该角色外貌的理解，**重新“拍摄”一段全新的单人说话视频**，而不是让这张设定稿动起来。`,
  )

  // 必须做到（正向约束）。
  parts.push(
    [
      '【视频必须做到】',
      `· 画面里自始至终**只有 ${name} 一个人**，头肩胸像正面特写、正对镜头；`,
      '· TA 在自然地说话，口型与台词逐字严丝合缝；',
      '· 表情生动、有戏剧张力（见下「表演」）；',
      '· 背景纯净中性，机位基本固定，只有极轻微的呼吸式浮动；',
      '· 写实清晰、五官与设定稿一致、整段稳定不变形。',
    ].join('\n'),
  )

  // 绝不要出现（负向约束）—— 直接点名最常见的崩坏形态。
  parts.push(
    [
      '【绝对不要出现】',
      '· 不要三视图 / 多视角 / 并排多个形象 / 分屏 / 网格 / 拼版（即：不要把参考图那种“多格排版”动起来）；',
      `· 不要同一画面里出现多个 ${name} 或任何多余人物；`,
      '· 不要全身像、半身以下、背面或纯侧面的“转身展示”；',
      '· 不要镜头在设定稿上平移 / 扫描 / 拉远去“看整张图”的运动；',
      '· 不要字幕、水印、文字标注、Logo、画框分割线。',
    ].join('\n'),
  )

  // 防御：参考图确为多分格拼版/全身并排时，再钉一句最硬的提醒。
  if (opts.reframeFromMultiPanel) {
    parts.push(
      `【重要】随附设定稿是多格并排的，请务必“重新取景”为单人头肩特写——只截取其中的正面形象来理解长相，输出画面里只有 ${name} 一个人的头与肩，绝不保留原图的分格 / 并排 / 全身 / 背面布局。`,
    )
  }

  // 角色信息/性格锚点。
  if (character.prompt?.trim()) {
    parts.push(`【角色设定】${clip(character.prompt, 220)}`)
  }
  if (character.anchor?.trim()) {
    parts.push(`【识别特征】${clip(character.anchor, 120)}`)
  }

  // 表演：这是「试镜表演」，要有戏剧张力与表现力，不是念稿。
  //   这条同时是「声音线」，驱动 generate_audio 出带情绪的念白（口型严丝合缝）。
  parts.push(
    `【表演】（关键：要像真正的演员在试镜入戏，有张力、有表现力，而非平淡念稿）${name} 直视镜头，带着符合其性格与处境的情绪投入地念出台词——情绪要可见且有层次（如台词所需的坚定 / 隐忍 / 锋利 / 脆弱 / 挑衅等），通过富有表现力的眼神、眉头与嘴角的细微变化、呼吸节奏、自然的头肩小幅律动、恰到好处的停顿来传递张力与角色魅力；口型与发声逐字对齐，气口、重音、语气起伏贴合台词含义。整体真实、有镜头存在感，避免僵硬、面瘫、机械、空洞；但仍是克制写实的表演，不做夸张鬼脸或大幅肢体动作。`,
  )
  parts.push(`【台词】（中文普通话，带情绪地逐字念出）：「${line}」`)

  // 画面规格收尾（不写秒数、不写打码）。
  parts.push(
    '【画面规格】3:4 竖画幅；有层次感的影棚布光（轻微明暗对比突出面部表情，避免死平均匀光）；画质稳定、面部清晰、表情生动有神。',
  )

  return parts.join('\n')
}

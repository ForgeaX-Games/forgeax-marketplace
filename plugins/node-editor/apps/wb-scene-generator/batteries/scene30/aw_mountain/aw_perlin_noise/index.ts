const PRIME_X = 501125321;
const PRIME_Y = 1136930381;

function hashR2(seed: number, xPrimed: number, yPrimed: number): number {
  let h = seed ^ xPrimed ^ yPrimed;
  h = Math.imul(h, 0x27d4eb2d);
  return h;
}

function gradCoordR2(seed: number, xPrimed: number, yPrimed: number, xd: number, yd: number): number {
  let h = hashR2(seed, xPrimed, yPrimed);
  h ^= h >> 15;
  h &= 127 << 1;
  return xd * GRADIENTS_2D[h]! + yd * GRADIENTS_2D[h | 1]!;
}

function interpQuintic(t: number): number {
  return t * t * t * (t * (t * 6 - 15) + 10);
}

function lerp(a: number, b: number, t: number): number {
  return a + t * (b - a);
}

function singlePerlinR2(seed: number, x: number, y: number): number {
  let x0 = Math.floor(x);
  let y0 = Math.floor(y);
  const xd0 = x - x0, yd0 = y - y0;
  const xd1 = xd0 - 1, yd1 = yd0 - 1;
  const xs = interpQuintic(xd0), ys = interpQuintic(yd0);
  x0 = Math.imul(x0, PRIME_X); y0 = Math.imul(y0, PRIME_Y);
  const x1 = x0 + PRIME_X, y1 = y0 + PRIME_Y;
  const xf0 = lerp(gradCoordR2(seed, x0, y0, xd0, yd0), gradCoordR2(seed, x1, y0, xd1, yd0), xs);
  const xf1 = lerp(gradCoordR2(seed, x0, y1, xd0, yd1), gradCoordR2(seed, x1, y1, xd1, yd1), xs);
  return lerp(xf0, xf1, ys) * 1.4247691104677813;
}

function calcFractalBounding(octaves: number, gain: number): number {
  let g = Math.abs(gain), amp = g, ampFractal = 1.0;
  for (let i = 1; i < octaves; i++) { ampFractal += amp; amp *= g; }
  return 1 / ampFractal;
}

function fractalFBm(seed: number, x: number, y: number, bounding: number): number {
  let s = seed, sum = 0, amp = bounding, cx = x, cy = y;
  for (let i = 0; i < OCTAVES; i++) {
    const noise = singlePerlinR2(s++, cx, cy);
    sum += noise * amp;
    amp *= lerp(1.0, Math.min(noise + 1, 2) * 0.5, 0.0);
    cx *= LACUNARITY; cy *= LACUNARITY;
    amp *= GAIN;
  }
  return sum;
}

// ── 主导出函数 ────────────────────────────────────────────────────────────────
export function awPerlinNoise(input: Record<string, unknown>): Record<string, unknown> {
  const refGrid = input.grid as number[][] | undefined;
  const rows = (Array.isArray(refGrid) && refGrid.length > 0) ? refGrid.length : 200;
  const cols = (Array.isArray(refGrid) && Array.isArray(refGrid[0])) ? refGrid[0].length : rows;
  const seed = Math.floor(typeof input.seed === "number" ? input.seed : 42);

  const bounding = calcFractalBounding(OCTAVES, GAIN);
  const grid: number[][] = [];

  for (let y = 0; y < rows; y++) {
    const row: number[] = [];
    for (let x = 0; x < cols; x++) {
      // 输入格子为 0（异形网格的空白区域）时，输出保持 0
      if (Array.isArray(refGrid) && refGrid[y]?.[x] === 0) {
        row.push(0);
      } else {
        const raw = fractalFBm(seed, x * FREQUENCY, y * FREQUENCY, bounding);
        row.push(raw * 0.5 + 0.5);
      }
    }
    grid.push(row);
  }

  return { grid };
}

function decodeTileSet(data: string[], w: number, h: number): number[][][] {
  return data.map((s) => {
    const tile: number[][] = [];
    for (let r = 0; r < h; r++)
      tile.push(Array.from(s.substring(r * w, (r + 1) * w)).map(Number));
    return tile;
  });
}

// ── Parse edge labels ───────────────────────────────────────────────────────

function parseEdgeLabels(raw: unknown, n: number): EdgeLabel[] | null {
  if (!Array.isArray(raw) || raw.length !== n) return null;
  const result: EdgeLabel[] = [];
  for (const item of raw) {
    if (typeof item !== "object" || item === null) return null;
    const obj = item as Record<string, unknown>;
    const N = String(obj.N ?? "");
    const S = String(obj.S ?? "");
    const E = String(obj.E ?? "");
    const W = String(obj.W ?? "");
    if (!N || !S || !E || !W) return null;
    result.push({ N, S, E, W });
  }
  return result;
}

// ── Main export ─────────────────────────────────────────────────────────────

export function wangTile(
  input: Record<string, unknown>,
): Record<string, unknown> {
  let tiles: number[][][];
  let weights: number[];
  let compat: Record<Dir, number[][]>;

  const rawTiles = input.tiles;
  const defaultSet = String(input.defaultSet ?? "horizontal");

  if (!Array.isArray(rawTiles) || rawTiles.length === 0) {
    if (defaultSet === "vertical") {
      tiles = decodeTileSet(V_DATA, V_W, V_H);
    } else {
      tiles = decodeTileSet(H_DATA, H_W, H_H);
    }
    weights = tiles.map(() => 1);
    compat = buildCompatFromPixels(tiles);
  } else {
    tiles = [];
    for (let i = 0; i < rawTiles.length; i++) {
      const t = rawTiles[i];
      if (!Array.isArray(t) || !Array.isArray((t as number[][])[0])) {
        return { error: `Each tile must be a 2D number array (tile ${i} is invalid)` };
      }
      tiles.push(t as number[][]);
    }
    const tH = tiles[0].length;
    const tW = tiles[0][0].length;
    for (let i = 1; i < tiles.length; i++) {
      if (tiles[i].length !== tH || tiles[i][0].length !== tW) {
        return {
          error: `All tiles must be ${tH}×${tW}. Tile ${i} has different size.`,
        };
      }
    }

    const rawW = input.weights;
    if (Array.isArray(rawW) && rawW.length === tiles.length) {
      weights = rawW.map((v) => Math.max(0.01, Number(v) || 1));
    } else {
      weights = tiles.map(() => 1);
    }

    const labels = parseEdgeLabels(input.edgeLabels, tiles.length);
    if (labels) {
      compat = buildCompatFromLabels(labels);
    } else {
      compat = buildCompatFromPixels(tiles);
    }
  }

  if (tiles.length === 0) {
    return { error: "No tiles available" };
  }

  const rows = Math.max(1, Math.min(64, Math.floor(Number(input.rows) || 8)));
  const cols = Math.max(1, Math.min(64, Math.floor(Number(input.cols) || 8)));
  const seed = Math.floor(Number(input.seed) || 0);

  const rng = new LCG(seed);
  const solution = solve(rows, cols, tiles.length, compat, weights, rng);

  // Count unsolved cells (greedy may produce -1 holes on constraint failure)
  let unsolved = 0;
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      if (solution[r][c] < 0) unsolved++;
    }
  }

  const { grid, valueGrid, regionCount } = buildOutput(solution, tiles, rows, cols);
  const result: Record<string, unknown> = {
    grid, valueGrid, regionCount, numTiles: rows * cols, unsolvedCells: unsolved,
  };
  if (unsolved > 0) {
    result.warning = `Greedy solver left ${unsolved}/${rows * cols} cells unsolved (rendered as background); consider relaxing constraints or increasing tile variety.`;
  }
  return result;
}

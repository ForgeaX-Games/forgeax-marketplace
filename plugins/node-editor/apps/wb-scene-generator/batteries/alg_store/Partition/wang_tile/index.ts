/**
 * wang_tile: Wang Tile constraint-based terrain generator.
 *
 * Two matching modes:
 *   1. Auto mode (default): derives adjacency from tile border pixel signatures.
 *   2. Label mode: uses explicit edge labels (from atlas converter or manual input).
 *
 * Built-in default tiles: 25 Noita inside.png tiles downsampled to 20×10 / 10×20.
 *
 * Internal tile encoding: 0 = background (solid), 1 = structure/passage, 2 = terrain.
 * Output grid: 0 = background (solid/wall), 1,2,3... = connected structure region IDs.
 * Output valueGrid: raw tile values (0 = bg, 1 = structure, 2 = terrain).
 */

class LCG {
  private s: bigint;
  constructor(seed: number) {
    this.s = BigInt(seed > 0 ? seed : 48271);
  }
  next(): bigint {
    this.s =
      (this.s * 6364136223846793005n + 1442695040888963407n) &
      0xffffffffffffffffn;
    return this.s;
  }
  float01(): number {
    return Number((this.next() >> 33n) % 1000000n) / 1000000;
  }
}

// ── Edge signature / label matching ─────────────────────────────────────────

type Dir = "N" | "S" | "E" | "W";
const DIRS: Dir[] = ["N", "S", "E", "W"];

function edgeSig(tile: number[][], dir: Dir): string {
  const h = tile.length;
  const w = tile[0].length;
  switch (dir) {
    case "N": return tile[0].join(",");
    case "S": return tile[h - 1].join(",");
    case "W": return tile.map((r) => r[0]).join(",");
    case "E": return tile.map((r) => r[w - 1]).join(",");
  }
}

interface EdgeLabel {
  N: string; S: string; E: string; W: string;
}

function buildCompatFromPixels(tiles: number[][][]): Record<Dir, number[][]> {
  const n = tiles.length;
  const sigs: Record<Dir, string[]> = { N: [], S: [], E: [], W: [] };
  for (const t of tiles) {
    for (const d of DIRS) sigs[d].push(edgeSig(t, d));
  }
  const c: Record<Dir, number[][]> = { N: [], S: [], E: [], W: [] };
  for (let i = 0; i < n; i++) {
    c.N.push([]); c.S.push([]); c.E.push([]); c.W.push([]);
    for (let j = 0; j < n; j++) {
      if (sigs.S[j] === sigs.N[i]) c.N[i].push(j);
      if (sigs.N[j] === sigs.S[i]) c.S[i].push(j);
      if (sigs.W[j] === sigs.E[i]) c.E[i].push(j);
      if (sigs.E[j] === sigs.W[i]) c.W[i].push(j);
    }
  }
  return c;
}

function buildCompatFromLabels(labels: EdgeLabel[]): Record<Dir, number[][]> {
  const n = labels.length;
  const c: Record<Dir, number[][]> = { N: [], S: [], E: [], W: [] };
  for (let i = 0; i < n; i++) {
    c.N.push([]); c.S.push([]); c.E.push([]); c.W.push([]);
    for (let j = 0; j < n; j++) {
      if (labels[j].S === labels[i].N) c.N[i].push(j);
      if (labels[j].N === labels[i].S) c.S[i].push(j);
      if (labels[j].W === labels[i].E) c.E[i].push(j);
      if (labels[j].E === labels[i].W) c.W[i].push(j);
    }
  }
  return c;
}

function intersect(a: number[], b: number[]): number[] {
  const s = new Set(b);
  return a.filter((x) => s.has(x));
}

// ── Greedy solver ───────────────────────────────────────────────────────────

function solve(
  rows: number,
  cols: number,
  nTiles: number,
  compat: Record<Dir, number[][]>,
  weights: number[],
  rng: LCG,
): number[][] {
  const all = Array.from({ length: nTiles }, (_, i) => i);
  const grid: number[][] = Array.from({ length: rows }, () =>
    new Array(cols).fill(-1),
  );

  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      let cands = all;
      if (r > 0 && grid[r - 1][c] >= 0) {
        cands = intersect(cands, compat.S[grid[r - 1][c]]);
      }
      if (c > 0 && grid[r][c - 1] >= 0) {
        cands = intersect(cands, compat.E[grid[r][c - 1]]);
      }
      if (cands.length === 0) { grid[r][c] = -1; continue; }

      const w = cands.map((i) => weights[i]);
      const total = w.reduce((a, b) => a + b, 0);
      let pick = rng.float01() * total;
      let chosen = cands[cands.length - 1];
      for (let i = 0; i < cands.length; i++) {
        pick -= w[i];
        if (pick <= 0) { chosen = cands[i]; break; }
      }
      grid[r][c] = chosen;
    }
  }
  return grid;
}

// ── Output grids ────────────────────────────────────────────────────────────
// Internal tile encoding: 0=background(solid), 1=structure/passage, 2=terrain
// Output grid:  0=background(wall), 1,2,3...=connected structure region IDs
// Output valueGrid: raw tile values (0=bg, 1=structure, 2=terrain)

function buildOutput(
  solution: number[][],
  tiles: number[][][],
  rows: number,
  cols: number,
): { grid: number[][]; valueGrid: number[][]; regionCount: number } {
  if (tiles.length === 0) return { grid: [[]], valueGrid: [[]], regionCount: 0 };
  const tH = tiles[0].length;
  const tW = tiles[0][0].length;
  const H = rows * tH;
  const W = cols * tW;

  // Phase 1: place tiles into raw grid (default 0 = background/solid)
  const raw: number[][] = Array.from({ length: H }, () => new Array(W).fill(0));
  for (let r = 0; r < rows; r++) {
    for (let c = 0; c < cols; c++) {
      const idx = solution[r][c];
      if (idx < 0 || idx >= tiles.length) continue;
      const t = tiles[idx];
      const oy = r * tH, ox = c * tW;
      for (let ty = 0; ty < tH; ty++)
        for (let tx = 0; tx < tW; tx++)
          raw[oy + ty][ox + tx] = t[ty][tx];
    }
  }

  // Phase 2: valueGrid = raw tile values directly
  const valueGrid: number[][] = Array.from({ length: H }, () => new Array(W).fill(0));
  for (let y = 0; y < H; y++)
    for (let x = 0; x < W; x++)
      valueGrid[y][x] = raw[y][x];

  // Phase 3: flood-fill connected non-zero (structure) regions → grid
  // 0 = background (stays 0), non-zero = structure/terrain → region IDs
  const grid: number[][] = Array.from({ length: H }, () => new Array(W).fill(0));
  for (let y = 0; y < H; y++)
    for (let x = 0; x < W; x++)
      if (raw[y][x] !== 0) grid[y][x] = -1;

  let regionId = 0;
  for (let y = 0; y < H; y++) {
    for (let x = 0; x < W; x++) {
      if (grid[y][x] !== -1) continue;
      regionId++;
      const queue: number[] = [y, x];
      grid[y][x] = regionId;
      let head = 0;
      while (head < queue.length) {
        const cy = queue[head++], cx = queue[head++];
        for (let d = 0; d < 4; d++) {
          const ny = cy + (d === 0 ? -1 : d === 1 ? 1 : 0);
          const nx = cx + (d === 2 ? -1 : d === 3 ? 1 : 0);
          if (ny < 0 || ny >= H || nx < 0 || nx >= W) continue;
          if (grid[ny][nx] !== -1) continue;
          grid[ny][nx] = regionId;
          queue.push(ny, nx);
        }
      }
    }
  }

  return { grid, valueGrid, regionCount: regionId };
}

